package kr.co.goms.module.common.model;

public class CommonBeanG {

    private String res_num;
    private String res_result;
    private String res_message;
    private String res_datetime;

    public CommonBeanG() {

    }

    public String getRes_num() {
        return res_num;
    }

    public void setRes_num(String res_num) {
        this.res_num = res_num;
    }

    public String getRes_result() {
        return res_result;
    }

    public void setRes_result(String res_result) {
        this.res_result = res_result;
    }

    public String getRes_message() {
        return res_message;
    }

    public void setRes_message(String res_message) {
        this.res_message = res_message;
    }

    public String getRes_datetime() {
        return res_datetime;
    }

    public void setRes_datetime(String res_datetime) {
        this.res_datetime = res_datetime;
    }

}
